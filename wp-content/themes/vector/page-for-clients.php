        <p>Информируем Вас о том, что в соответствии с п. 14 ст. 7 Федерального закона от 07.08.2001 года № 115-ФЗ «О противодействии легализации (отмыванию) доходов, полученных преступным путем, и финансированию терроризма» (далее – Федеральный закон) клиенты обязаны предоставлять организациям, осуществляющим операции с денежными средствами или иным имуществом, информацию, необходимую для исполнения указанными организациями требований Федерального закона, включая информацию о своих выгодоприобретателях, учредителях (участниках) и бенефициарных владельцах, а также о своем статусе доверительного собственника (управляющего) иностранной структуры без образования юридического лица, протектора.</p>
        <p>В целях исполнения Федерального закона бенефициарным владельцем признается «физическое лицо, которое в конечном счете прямо или косвенно (через третьих лиц), владеет (имеет преобладающее участие более 25 процентов в капитале) клиентом – юридическим лицом либо имеет возможность контролировать действия клиента. Бенефициарным владельцем клиента - физического лица считается это лицо, за исключением случаев, если имеются основания полагать, что бенефициарным владельцем является иное физическое лицо».</p>
